""" There are four main levels of measurement: nominal, ordinal, interval, and ratio. 

Nominal: This is the lowest level of measurement and is used for categorical data where values represent categories or names. Data at this level cannot be ordered or compared using numerical methods.
Example: Gender (male or female), Hair color (black, brown, blonde, etc.)

Ordinal: This level of measurement is used for data that can be ranked or ordered, but the differences between values may not be meaningful.
Example: Educational level (elementary, high school, college), Rank in a competition (first, second, third, etc.)

Interval: This level of measurement is used for data that can be ordered and the difference between values is meaningful. However, there is no true zero point.
Example: Temperature measured in Celsius or Fahrenheit, Dates on a calendar.

Ratio: This level of measurement is used for data that can be ordered, the difference between values is meaningful, and there is a true zero point.
"""


