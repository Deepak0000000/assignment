"""There are four main types of data: nominal, ordinal, interval, and ratio data. Each type of data is defined by the properties of the values that are measured or observed.

Nominal data: This type of data is used to categorize or name things. The values are not ordered, and there is no inherent numerical relationship between them.
Example: The colors of cars in a parking lot.

Ordinal data: This type of data has an inherent order, but the difference between the values is not meaningful or equal.
Example: The rankings of runners in a race.

Interval data: This type of data has an inherent order and the difference between values is meaningful and equal, but there is no true zero point.
Example: Temperature measured in degrees Celsius.

Ratio data: This type of data has an inherent order, the difference between values is meaningful and equal, and there is a true zero point.
Example: Height measured in centimeters."""


