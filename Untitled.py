"""Descriptive and inferential statistics are two broad categories of statistical analysis used in research and data analysis.

Descriptive statistics are used to describe and summarize data using measures such as mean, median, mode, range, and 
standard deviation.
Descriptive statistics provide a clear and concise summary of the data, and are useful for presenting the data in a way
that is easily understandable. They can also be used to identify patterns, trends, or outliers in the data. For example,
the average height of students in a class, the number of cars sold in a month, or the distribution of ages in a population 
are all examples of descriptive statistics.

Inferential statistics, on the other hand, are used to draw conclusions or make inferences about a population based on
a sample of data. Inferential statistics involve using probability theory and hypothesis testing to make generalizations
about the population. They are used to test hypotheses, make predictions, and estimate parameters of interest. For example, 
testing whether a new drug is effective in treating a disease, determining if there is a significant difference between the
mean incomes of two groups, or predicting the number of voters who will support a political candidate are all examples of 
inferential statistics."""


