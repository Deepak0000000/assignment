"""Mean: The mean is the arithmetic average of a dataset and is calculated by summing all values in the dataset and dividing by the number of values. The mean is a useful measure of central tendency for datasets that are normally distributed and have no outliers.

Median: The median is the middle value of a dataset and is calculated by arranging the values in ascending order and selecting the middle value. The median is a useful measure of central tendency for datasets that have outliers or are skewed.

Mode: The mode is the value that occurs most frequently in a dataset. The mode is a useful measure of central tendency for datasets with categorical or nominal data.

Range: The range is the difference between the maximum and minimum values in a dataset. The range is a useful measure of variability as it provides a quick summary of the spread of the data.

Variance: The variance is a measure of how spread out a dataset is from the mean. It is calculated by summing the squared differences between each value and the mean, and dividing by the number of values. A larger variance indicates a more spread out dataset, while a smaller variance indicates a more tightly clustered dataset.

Standard deviation: The standard deviation is the square root of the variance and is another measure of how spread out a dataset is from the mean. The standard deviation is a useful measure of variability as it is in the same units as the data and is easily interpretable."""

