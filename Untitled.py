"""The box of the plot represents the interquartile range (IQR), which is the range of values from the 
first quartile (Q1) to the third quartile (Q3). The line within the box represents the median, and the 
whiskers extend to the minimum and maximum values within a specified range. Any data points outside of
the whiskers are considered outliers and are displayed as individual points.

By displaying the range of the data in a visual format, the box plot allows us to easily compare multiple datasets
and identify any potential outliers or patterns in the data. It is particularly useful for comparing datasets with 
different distributions or for identifying any skewness or asymmetry in the data."""


